const { pool } = require('../config/database');

class Etapa {
    static getAll() {
        return new Promise((resolve, reject) => {
            pool.query('SELECT * FROM etapa', (err, result) => {
                if (err) {
                    reject(err);
                } else {
                    resolve(result);
                }
            });
        });
    }

    static getById(id) {
        return new Promise((resolve, reject) => {
            pool.query('SELECT * FROM etapa WHERE id = ?', [id], (err, result) => {
                if (err) {
                    reject(err);
                } else {
                    resolve(result);
                }
            });
        });
    }

}

module.exports = Etapa;
